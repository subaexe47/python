n = input()
if (n == 'a' or n == 'A' or n == 'e' or n == 'E' or n =='i' or n =='I' or n == 'o' or n == 'O' or n == 'u' or n == 'U'):
  print('Vowel')
elif (n != 'a' or n != 'A' or n != 'e' or n != 'E' or n !='i' or n !='I' or n != 'o' or n != 'O' or n != 'u' or n != 'U'):
  print('Consonant')
else:
  print('Invalid')
